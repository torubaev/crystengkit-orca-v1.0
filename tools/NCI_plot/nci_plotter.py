        clean = str(path)
        recent = [clean] + [
            item for item in self.recent_wavefunction_files()
            if os.path.normcase(item) != os.path.normcase(clean)
        ]
        self.config["recent_wavefunction_files"] = recent[:MAX_RECENT_FILES]
        try:
            self.wavefunction_combo.configure(values=self.config["recent_wavefunction_files"])
        except Exception:
            pass
