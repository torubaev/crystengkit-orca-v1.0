def contrast_text_color(background: str) -> str:
    colors = {
        "white": "black",
        "lightgray": "black",
        "gray": "white",
        "black": "white",
    }
    bg = str(background or "").strip().lower()
    if bg in colors:
        return colors[bg]
    if bg.startswith("#") and len(bg) in {4, 7}:
        try:
            if len(bg) == 4:
                r = int(bg[1] * 2, 16)
                g = int(bg[2] * 2, 16)
                b = int(bg[3] * 2, 16)
            else:
                r = int(bg[1:3], 16)
                g = int(bg[3:5], 16)
                b = int(bg[5:7], 16)
            luminance = (0.2126 * r + 0.7152 * g + 0.0722 * b) / 255.0
            return "black" if luminance > 0.55 else "white"
        except Exception:
            pass
    return "black"


