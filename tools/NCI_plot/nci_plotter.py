@dataclass
class CubeData:
    path: Path
    origin: np.ndarray
    axes: np.ndarray
    shape: tuple[int, int, int]
    atom_numbers: list[int]
    atom_coords: np.ndarray
    values: np.ndarray


class CubeParseError(Exception):
    """Raised when a cube file cannot be parsed safely."""


class CubeParser:
    BOHR_TO_ANG = 0.529177210903

    @staticmethod
    def parse(path: Path) -> CubeData:
        if not path.exists():
            raise CubeParseError(f"Cube file not found: {path}")

        try:
            lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
        except Exception as exc:
            raise CubeParseError(f"Could not read cube file: {path}") from exc

        if len(lines) < 6:
            raise CubeParseError(f"Malformed cube file: too few lines: {path}")

        try:
            atom_line = lines[2].split()
            natoms_raw = int(float(atom_line[0]))
            natoms = abs(natoms_raw)

            origin = np.array(
                [float(atom_line[1]), float(atom_line[2]), float(atom_line[3])],
                dtype=float,
            )

            axes = []
            shape = []

            for i in range(3):
                parts = lines[3 + i].split()
                n = int(float(parts[0]))
                shape.append(abs(n))
                axes.append([float(parts[1]), float(parts[2]), float(parts[3])])

            axes_arr = np.array(axes, dtype=float)
            shape_tuple = (shape[0], shape[1], shape[2])

            atom_numbers: list[int] = []
            atom_coords_list = []

            for i in range(natoms):
                parts = lines[6 + i].split()
                atom_numbers.append(int(float(parts[0])))
                atom_coords_list.append(
                    [float(parts[2]), float(parts[3]), float(parts[4])]
                )

            atom_coords = np.array(atom_coords_list, dtype=float)

            data_start = 6 + natoms
            raw_values = []

            for line in lines[data_start:]:
                for token in line.split():
                    raw_values.append(float(token.replace("D", "E").replace("d", "e")))

            expected = shape_tuple[0] * shape_tuple[1] * shape_tuple[2]

            if len(raw_values) < expected:
                raise CubeParseError(
                    f"Malformed cube file: expected {expected} grid values, "
                    f"found {len(raw_values)}: {path}"
                )

            values = np.array(raw_values[:expected], dtype=float).reshape(
                shape_tuple, order="C"
            )

            if natoms_raw > 0:
                origin = origin * CubeParser.BOHR_TO_ANG
                axes_arr = axes_arr * CubeParser.BOHR_TO_ANG
                atom_coords = atom_coords * CubeParser.BOHR_TO_ANG

            return CubeData(
                path=path,
                origin=origin,
                axes=axes_arr,
                shape=shape_tuple,
                atom_numbers=atom_numbers,
                atom_coords=atom_coords,
                values=values,
            )

        except CubeParseError:
            raise
        except Exception as exc:
            raise CubeParseError(f"Malformed cube file: {path}") from exc


def screen_fraction_height(widget: tk.Misc, fraction: float = 0.80, fallback: int = 760) -> int:
    try:
        return max(1, int(widget.winfo_screenheight() * fraction))
    except Exception:
        return fallback


def load_header_icon(path: Path, max_size: int = 56) -> Optional[tk.PhotoImage]:
    if not path.is_file():
        return None
    img = tk.PhotoImage(file=str(path))
    factor = max(1, int(max(img.width() / max_size, img.height() / max_size) + 0.999))
    if factor > 1:
        img = img.subsample(factor, factor)
    return img


