def set_scalar_bar_text_color(plotter, text_color: str, title: str | None = None) -> None:
    try:
        scalar_bars = getattr(plotter, "scalar_bars", {})
        scalar_bar = scalar_bars.get(title) if title else None
        if scalar_bar is None:
            bars = list(scalar_bars.values())
            if not bars:
                return
            scalar_bar = bars[0]
        import pyvista as pv
        rgb = pv.Color(text_color).float_rgb
        title_prop = scalar_bar.GetTitleTextProperty()
        label_prop = scalar_bar.GetLabelTextProperty()
        title_prop.SetColor(*rgb)
        label_prop.SetColor(*rgb)
        title_prop.BoldOn()
        label_prop.BoldOn()
        title_prop.SetFontSize(16)
        label_prop.SetFontSize(12)
    except Exception:
        pass


