    def load_config(self) -> dict:
        if CONFIG_FILE.exists():
            try:
                config = json.loads(CONFIG_FILE.read_text(encoding="utf-8"))
            except Exception:
                return {}
            template = str(config.get("multiwfn_template", "")).strip()
            if template == "q" or template == LEGACY_SAFE_MULTIWFN_TEMPLATE.strip():
                config["multiwfn_template"] = DEFAULT_MULTIWFN_TEMPLATE
            return config
        return {}

